<?php

declare(strict_types=1);

/**
 * Single source of truth for the preset-dependent Powermail form classes.
 *
 * Instead of regenerating every Powermail template, the sync script emits ONE
 * Fluid partial — Partials/Form/ShadcnClass.html — that maps a logical name
 * (input, textarea, select, …) to the shadcn recipe-derived class string. The
 * hand-maintained Powermail templates reference it inline, e.g.:
 *
 *   class="powermail_input {f:render(partial: 'Form/ShadcnClass', arguments: {name: 'input'}) -> f:format.trim()}"
 *
 * So changing the shadcn/create preset + re-running the sync reflows the whole
 * form (controls from the input/textarea/button recipes, radius from {{R}}),
 * and ComponentStructureTest's --check enforces the partial stays in sync.
 *
 * @return array<string, string> repository-relative path => file content
 */
function powermailFluidTargets(array $recipes): array
{
    return [
        'Resources/Private/Extensions/Powermail/Partials/Form/ShadcnClass.html' => renderShadcnClassPartial($recipes),
    ];
}

/**
 * @return array<string, string> logical name => recipe-derived class string
 */
function powermailClassMap(array $recipes): array
{
    $radius = extractPowermailRadius($recipes['input']);

    return [
        'input' => normalizeClass($recipes['input'] . ' text-foreground'),
        'textarea' => normalizeClass($recipes['textarea'] . ' min-w-0 text-foreground'),
        'select' => normalizeClass(
            'flex min-h-8 w-full min-w-0 items-center justify-between gap-1.5 ' . $radius
            . ' border border-input bg-transparent py-1 pr-2 pl-2.5 text-xs text-foreground transition-colors outline-none'
            . ' focus-visible:border-ring focus-visible:ring-1 focus-visible:ring-ring/50 disabled:cursor-not-allowed disabled:opacity-50'
            . ' aria-invalid:border-destructive aria-invalid:ring-1 aria-invalid:ring-destructive/20'
            . ' dark:bg-input/30 dark:aria-invalid:border-destructive/50 dark:aria-invalid:ring-destructive/40'
        ),
        'checkbox' => normalizeClass(
            'size-4 ' . $radius . ' border border-input bg-transparent text-primary accent-primary outline-none'
            . ' focus-visible:ring-1 focus-visible:ring-ring/50 dark:bg-input/30'
        ),
        'radio' => normalizeClass(
            'size-4 rounded-full border border-input bg-transparent text-primary accent-primary outline-none'
            . ' focus-visible:ring-1 focus-visible:ring-ring/50 dark:bg-input/30'
        ),
        'file' => normalizeClass(
            'flex min-h-24 w-full cursor-pointer items-center ' . $radius . ' border border-dashed border-input bg-muted/30 px-3 py-3 text-sm text-muted-foreground transition-colors outline-none'
            . ' file:me-3 file:' . $radius . ' file:border-0 file:bg-primary file:px-2.5 file:py-1.5 file:text-xs file:font-medium file:text-primary-foreground'
            . ' hover:bg-muted/50 focus-visible:border-ring focus-visible:ring-1 focus-visible:ring-ring/50'
            . ' aria-invalid:border-destructive aria-invalid:ring-1 aria-invalid:ring-destructive/20 dark:bg-input/30'
        ),
        'btnPrimary' => powermailButtonClass($recipes['button'], 'default'),
        'btnSecondary' => powermailButtonClass($recipes['button'], 'outline'),
        'radius' => $radius,
    ];
}

/**
 * Compose a button class from the shadcn button recipe: base + variant + default size.
 *
 * @param array<string, mixed> $button
 */
function powermailButtonClass(array $button, string $variant): string
{
    return normalizeClass(
        ($button['base'] ?? '') . ' '
        . ($button['variants'][$variant] ?? '') . ' '
        . ($button['sizes']['default'] ?? '')
    );
}

function renderShadcnClassPartial(array $recipes): string
{
    // Store the raw class strings: the consuming templates output the partial via
    // an inline expression ({f:render(...) -> f:format.trim()}) which applies
    // Fluid's default HTML escaping once, yielding valid single-escaped markup.
    // Escaping here as well would double-escape '&' and "'" in the button selectors.
    $cases = '';
    foreach (powermailClassMap($recipes) as $name => $class) {
        $cases .= sprintf("    <f:case value=\"%s\">%s</f:case>\n", $name, $class);
    }

    return '<html xmlns:f="http://typo3.org/ns/TYPO3/CMS/Fluid/ViewHelpers" data-namespace-typo3-fluid="true">' . "\n"
        . "<f:comment>\n"
        . "    Generated by Build/Scripts/sync-shadcn-fluid-primitives.php (Powermail set).\n"
        . "    Single source of truth for preset-dependent Powermail form classes.\n"
        . "    Edit Build/Scripts/powermail-fluid-templates.php and re-run the sync; do not hand-edit.\n"
        . "</f:comment>\n"
        . '<f:argument name="name" type="string" />' . "\n"
        . '<f:switch expression="{name}">' . "\n"
        . $cases
        . '</f:switch>' . "\n"
        . '</html>' . "\n";
}

function extractPowermailRadius(string $inputRecipe): string
{
    if (preg_match('/(?<![\w:-])rounded-[a-z0-9-]+/', $inputRecipe, $match)) {
        return $match[0];
    }

    return 'rounded-md';
}
